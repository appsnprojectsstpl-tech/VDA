-- Create function to update KPI (for future use)
CREATE OR REPLACE FUNCTION update_kpi(
    p_module VARCHAR,
    p_kpi_name VARCHAR,
    p_value NUMERIC
)
RETURNS VOID AS $$
BEGIN
    UPDATE kpi_summary 
    SET kpi_value = p_value,
        last_updated = NOW()
    WHERE module = p_module AND kpi_name = p_kpi_name;
END;
$$ LANGUAGE plpgsql;

-- Grant execute permission to authenticated users
GRANT EXECUTE ON FUNCTION update_kpi(VARCHAR, VARCHAR, NUMERIC) TO authenticated;

