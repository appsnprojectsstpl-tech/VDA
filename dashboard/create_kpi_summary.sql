-- Create kpi_summary table for Vedavathi Dashboard
-- Run this in your InsForge backend using the SQL runner

-- Drop table if exists (for fresh install)
DROP TABLE IF EXISTS kpi_summary CASCADE;

-- Create kpi_summary table
CREATE TABLE kpi_summary (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    module VARCHAR(100) NOT NULL,
    kpi_name VARCHAR(255) NOT NULL,
    kpi_value NUMERIC(15, 2) DEFAULT 0,
    unit VARCHAR(50) DEFAULT 'count',
    trend VARCHAR(20) DEFAULT 'stable',  -- 'up', 'down', 'stable'
    trend_change NUMERIC(5, 2) DEFAULT 0,
    last_updated TIMESTAMPTZ DEFAULT NOW(),
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Enable RLS (Row Level Security)
ALTER TABLE kpi_summary ENABLE ROW LEVEL SECURITY;

-- Allow anon access (read)
CREATE POLICY "Allow for all" 
ON read access kpi_summary FOR SELECT 
TO anon, authenticated 
USING (true);

-- Allow authenticated users to insert/update
CREATE POLICY "Allow authenticated insert/update" 
ON kpi_summary FOR ALL 
TO authenticated 
USING (true)
WITH CHECK (true);

